#include "Buildings.h"
#include <string>
using namespace std;

#ifndef INDUSTRIALBUILDING_H
#define INDUSTRIALBUILDING_H

/**
 * @class IndustrialBuilding
 * @brief Represents an industrial building, inheriting from Buildings.
 */
class IndustrialBuilding : public Buildings {

protected:
    string buildingName;          /**< Name of the industrial building. */
    string buildingType;          /**< Type of the industrial building. */
    Citizen* listOfCitizens;      /**< List of citizens associated with the building. */

    double maintenanceCost;       /**< Maintenance cost for the building. */
    int powerReq;                 /**< Power requirement of the building. */
    int waterReq;                 /**< Water requirement of the building. */
    int sewageCost;               /**< Sewage cost for the building. */
    ResourceMediator* resources;  /**< Mediator for managing resources. */

    float sales;                  /**< Sales generated by the building. */
    float profit;                 /**< Profit generated by the building. */
    int employment;               /**< Employment count in the building. */

    State* state;                 /**< Current state of the building. */

public:
    /**
     * @brief Cleans the sewage of the industrial building.
     * @return true if sewage cleaning was successful, false otherwise.
     */
    virtual bool cleanSewage() = 0;

    /**
     * @brief Cleans the waste of the industrial building.
     * @return true if waste cleaning was successful, false otherwise.
     */
    virtual bool cleanWaste() = 0;

    /**
     * @brief Gets the type of the industrial building.
     * @return The type of the building as a string.
     */
    virtual string getBuildingType() = 0;

    /**
     * @brief Gets the number of jobs created by the industrial building.
     * @return The number of jobs as an integer.
     */
    virtual int getJobsCreated() = 0;

    /**
     * @brief Gets the maintenance cost of the industrial building.
     * @return The maintenance cost as a double.
     */
    virtual double getMaintenanceCost() = 0;

    /**
     * @brief Gets the name of the industrial building.
     * @return The name of the building as a string.
     */
    virtual string getName() = 0;

    /**
     * @brief Gets the power requirement of the industrial building.
     * @return The power requirement as an integer.
     */
    virtual int getPowerReq() = 0;

    /**
     * @brief Gets the sewage cost of the industrial building.
     * @return The sewage cost as an integer.
     */
    virtual int getSewageCost() = 0;

    /**
     * @brief Gets the water requirement of the industrial building.
     * @return The water requirement as an integer.
     */
    virtual int getWaterReq() = 0;

    /**
     * @brief Performs the main operation of the industrial building.
     */
    virtual void operation() = 0;

    /**
     * @brief Pays the maintenance cost for the industrial building.
     * @return true if payment was successful, false otherwise.
     */
    virtual bool payMaintenance() = 0;

    /**
     * @brief Receives power for the industrial building.
     * @return true if power was received successfully, false otherwise.
     */
    virtual bool receivePower() = 0;

    /**
     * @brief Receives water for the industrial building.
     * @return true if water was received successfully, false otherwise.
     */
    virtual bool receiveWater() = 0;

    /**
     * @brief Requests the current state of the industrial building.
     */
    virtual void requestState() = 0;

    /**
     * @brief Gets the sales generated by the industrial building.
     * @return Sales amount as a float.
     */
    virtual float getSales() = 0;

    /**
     * @brief Gets the profit generated by the industrial building.
     * @return Profit amount as a float.
     */
    virtual float getProfit() = 0;

    /**
     * @brief Pays a citizen employed in the industrial building.
     * @return The payment amount as an integer.
     */
    virtual int payCitizen() = 0;
};

#endif

